                            <p>The record, and briefs of argument to be filed by parties, form what the Court of Appeal will consider and review in deciding the appeal filed by the AGF (on behalf of the Federal Government) against the CCT judgment of June 14. The number is: CA/A/460/2017. With the appeal now entered and number allocated, the appellant, by the Rules of the Court of Appeal, It was learnt yesterday that the transmission of the record started on Wednesday, but was completed yesterday, following which the Appeal Court entered the appeal on its list and allocated a number to it. </p>
                            <h3>This is a h3 headline</h3>
                            <p>is to proceed to file its brief, serve on the respondent, who in turn, will file its response and serve the appellant (the process of briefs exchange), following which the court will fix a date for hearing of the appeal. In a notice of appeal it filed on June 20, 2017 the appellant stated that the judgment “effectively” overruled previous decisions of the Court of Appeal delivered with respect to Saraki’s trial and other criminal cases.</p>
