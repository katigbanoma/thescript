                            <p><span class="mh-dropcap">T</span>
                              he office of the Attorney General of the Federation (AGF) yesterday completed the transmission of record of proceedings at the Code of Conduct Tribunal (CCT) to the Court of Appeal in Abuja in relation to the appeal it filed against the acquittal of Senate President Bukola Saraki by the CCT.</p>
                            <div class="mh-content-ad">
                                <a href="#">
                                  <img src="images/ad-300x250-mh-magazine.png" width="300" height="250" />
